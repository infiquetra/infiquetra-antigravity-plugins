RENDERERS = {
    "lifecycle-atlas": render_lifecycle_atlas,
    "state-readiness-ladder": render_state_readiness_ladder,
    "command-matrix": render_command_matrix,
    "ownership-boundary-map": render_ownership_boundary_map,
}


def render_all(model: dict[str, Any]) -> dict[str, str]:
    missing = set(RENDERERS) - set(model.get("visuals", {}))
    if missing:
        raise ValueError(f"model missing visual definitions: {sorted(missing)}")
    return {visual_id: renderer(model) for visual_id, renderer in RENDERERS.items()}


def _target_path(model: dict[str, Any], visual_id: str, output_dir: Path | None) -> Path:
    relative_path = cast("str", model["visuals"][visual_id]["file"])
    filename = Path(relative_path).name
    if output_dir is not None:
        return output_dir / filename
    return REPO_ROOT / relative_path


def write_assets(
    model: dict[str, Any], *, output_dir: Path | None = None, check: bool = False
) -> int:
    rendered = render_all(model)
    failures: list[str] = []

    for visual_id, svg in rendered.items():
        target = _target_path(model, visual_id, output_dir)
        if check:
            if not target.exists():
                failures.append(f"missing {target}")
                continue
            if target.read_text(encoding="utf-8") != svg:
                failures.append(f"stale {target}")
            continue
        target.parent.mkdir(parents=True, exist_ok=True)
        target.write_text(svg, encoding="utf-8")

    if failures:
        for failure in failures:
            print(failure, file=sys.stderr)
        return 1
    return 0


def _build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--model", type=Path, default=MODEL_PATH)
    parser.add_argument("--output-dir", type=Path)
    parser.add_argument("--check", action="store_true")
    return parser


def main(argv: list[str] | None = None) -> int:
    args = _build_parser().parse_args(argv)
    model = _load_model(args.model)
    return write_assets(model, output_dir=args.output_dir, check=args.check)


if __name__ == "__main__":
    sys.exit(main())
